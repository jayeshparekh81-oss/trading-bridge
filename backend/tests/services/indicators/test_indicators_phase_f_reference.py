_FIX_DIR = Path(__file__).parent / "fixtures"
_INPUT_CSV = _FIX_DIR / "nifty_100_bars_5m.csv"

# Tolerance choice — calibrated to catch real convention bugs, not
# float-accumulation noise. Cross-verification of pandas-ta-classic
# vs hand-rolled Pine reference (see fixture generator) showed natural
# noise of 1e-8 to 1e-14 across all 5 indicators. The known BB
# convention bug (``bb.py:67-72`` correction inflates bands by
# sqrt(20/19) ≈ 2.6% at length=20) produces ~2 absolute on NIFTY-
# priced bars (price ~22000, band offset ~85, correction adds ~2).
# atol=0.01 (one paisa on ₹22000) sits in the middle: 6 orders of
# magnitude above natural noise, 2 orders of magnitude below the BB
# bug signal — catches the bug, doesn't false-positive on epsilon.
_RTOL = 1e-7
_ATOL = 1e-2


def _assert_series_matches_pine(
    series_name: str,
    got: np.ndarray,
    expected: list[float | None],
) -> None:
    """Compare a TRADETRI ndarray against a Pine-reference list.

    Both must have the same length. ``None`` in ``expected`` means
    NaN — the corresponding ``got`` value must also be NaN. For
    non-None expected values, compare to ``got`` under
    ``np.testing.assert_allclose(rtol, atol)``.

    Surfaces a clear per-bar error message on mismatch so triage
    doesn't require re-running with extra prints.
    """
    assert len(got) == len(expected), (
        f"{series_name}: length {len(got)} != expected {len(expected)}"
    )
    for i, (g, e) in enumerate(zip(got.tolist(), expected)):
        if e is None:
            assert math.isnan(g), (
                f"{series_name}[{i}]: expected NaN, got {g}"
            )
        else:
            assert not math.isnan(g), (
                f"{series_name}[{i}]: expected {e}, got NaN"
            )
            # Use np.allclose semantics: |g - e| <= atol + rtol * |e|
            tol = _ATOL + _RTOL * abs(e)
            assert abs(g - e) <= tol, (
                f"{series_name}[{i}]: expected {e:.10f}, got {g:.10f}, "
                f"diff {abs(g - e):.3e}, tol {tol:.3e}"
            )


