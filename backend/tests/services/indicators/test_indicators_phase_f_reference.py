"""Phase F reference tests — TRADETRI indicators vs Pine ground truth.

Independent ground-truth tests for the 5 MVP indicators. The expected
CSVs at :file:`fixtures/*_pine_expected.csv` are generated by
:file:`fixtures/_generate_phase_f_fixtures.py` using
``pandas-ta-classic`` (cross-verified against hand-rolled Pine
reference math from the Phase F deviation analysis — see
``PHASE_F_DEVIATION_ANALYSIS.md`` + ``PHASE_F_DEVIATION_ANALYSIS_PART2.md``
at repo root). The hand-roll matches TA-Lib's NaN convention exactly,
so the fixtures align positionally with TRADETRI's output.

Coverage gap these tests close
    The existing per-indicator tests at :file:`test_{sma,ema,rsi,macd,bb}.py`
    use fixture CSVs (``shared_input.csv`` + ``{name}_expected.csv``)
    that were generated FROM TA-Lib output — they're self-referential
    and cannot catch a TA-Lib regression or a wrong-direction
    post-processing correction (see Part 1 audit on BB band-width
    inflation). These reference tests use an INDEPENDENT source
    (pandas-ta-classic + hand-rolled Pine math) and break that
    tautology.

Tolerance
    ``rtol=1e-4, atol=1e-6`` — generous enough to absorb float
    accumulation differences between TA-Lib's C kernel and pandas-ta-
    classic's pandas/numpy path (the cross-verification in the
    fixture generator showed max diffs of 1e-8 to 1e-14 across all
    indicators). Tighter than 0.01% — well below any signal-relevant
    threshold.

How to regenerate the fixtures (manual, throwaway venv)::

    python3 -m venv /tmp/phase-f-venv
    /tmp/phase-f-venv/bin/pip install numpy pandas ta-lib==0.6.4 \\
        pydantic pandas-ta-classic
    cd backend
    PYTHONPATH=. /tmp/phase-f-venv/bin/python \\
        tests/services/indicators/fixtures/_generate_phase_f_fixtures.py
"""

